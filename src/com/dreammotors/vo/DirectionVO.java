package com.dreammotors.vo;

public class DirectionVO {
	private String clocAddr;
	private String clocTell;
	private String clocMapUrl;
	public String getClocAddr() {
		return clocAddr;
	}
	public void setClocAddr(String clocAddr) {
		this.clocAddr = clocAddr;
	}
	public String getClocTell() {
		return clocTell;
	}
	public void setClocTell(String clocTell) {
		this.clocTell = clocTell;
	}
	public String getClocMapUrl() {
		return clocMapUrl;
	}
	public void setClocMapUrl(String clocMapUrl) {
		this.clocMapUrl = clocMapUrl;
	}
	@Override
	public String toString() {
		return "DirectionVO [clocAddr=" + clocAddr + ", clocTell=" + clocTell + ", clocMapUrl=" + clocMapUrl + "]";
	}
}
